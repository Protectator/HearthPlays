namespace HearthPlays {
	export class Entity {
        private tags: {[tag: string]: number};
        
        public getTag(tagName: string): number {
            return this.tags[tagName];
        }
        
        public setTag(tagName: string, value: number): void {
            this.tags[tagName] = value;
        }
	}
}